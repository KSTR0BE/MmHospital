package kr.or.ddit.doctor.vo;

import java.time.LocalDate;

public class DiagnosisDetailVO {
	private int dg_no;
	private String mem_nm;
	private String dg_tit;
	private String dg_con;
	private LocalDate dg_dt;
	private String md_nm;
	private String cl_nm;
	private String dt_nm;
	
	public int getDg_no() {
		return dg_no;
	}
	public void setDg_no(int dg_no) {
		this.dg_no = dg_no;
	}
	public String getMem_nm() {
		return mem_nm;
	}
	public void setMem_nm(String mem_nm) {
		this.mem_nm = mem_nm;
	}
	public String getDg_tit() {
		return dg_tit;
	}
	public void setDg_tit(String dg_tit) {
		this.dg_tit = dg_tit;
	}
	public String getDg_con() {
		return dg_con;
	}
	public void setDg_con(String dg_con) {
		this.dg_con = dg_con;
	}
	public LocalDate getDg_dt() {
		return dg_dt;
	}
	public void setDg_dt(LocalDate dg_dt) {
		this.dg_dt = dg_dt;
	}
	public String getMd_nm() {
		return md_nm;
	}
	public void setMd_nm(String md_nm) {
		this.md_nm = md_nm;
	}
	public String getCl_nm() {
		return cl_nm;
	}
	public void setCl_nm(String cl_nm) {
		this.cl_nm = cl_nm;
	}
	public String getDt_nm() {
		return dt_nm;
	}
	public void setDt_nm(String dt_nm) {
		this.dt_nm = dt_nm;
	}
	
	
	
	
	
	
	
}
